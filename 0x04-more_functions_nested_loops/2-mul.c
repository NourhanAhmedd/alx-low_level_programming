/**
 * mul - Function that multiplies two integers.
 *
 * @a: Takes the first input for the function.
 * @b: Takes the second input for the function.
 *
 * Return: It will return the result of the multiplication
*/

int mul(int a, int b)
{
	int result;

	result = a * b;

	return (result);
}
